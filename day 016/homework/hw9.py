


# 9)შექმენით კალკულატორი როგორიც ჩვენ გავაკეთეთ,დაუმატეთ სხვა მათემატიკური ოპერატორები,ასევე დაუმატეთ შედარების ოპერატორებიც 


num1=int(input('enter number'))

num2=int(input('enter number'))

op=input('enter + - * / ** %:')

if op=="+":
    print(num1+num2)
elif op=="-":
    print(num1-num2)
elif op=='*':
    print(num1*num2)
elif op=='**':
    print(num1**num2)
elif op=='/':
    print(num1/num2)
elif op=='/':
    print(num1/num2)
elif op=='%':
    print(num1%num2)


