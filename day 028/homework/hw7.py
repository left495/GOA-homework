

# 7) მომხმარებელს შეაყვანინე რიცხვი 1–5. დაბეჭდე სიტყვებით, მაგალითად: 1 → One 2 → Two გამოიყენე if / elif / else

while True:
    num=int(input('enter number 0/5 :'))

    if num==1:
        print('one')
        break
    elif num==2:
        print('two')
        break
    elif num==3:
        print('three')
        break
    elif num==4:
        print('four')
        break
    elif num==5:
        print('five')
        break
